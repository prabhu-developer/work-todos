    {
        $request->validate([
            'title' => 'required|max:350'
        ]); 

        $this->todos->create([
            'title'   => $request->title,
            'user_id' => auth()->user()->id
        ]);

        return redirect(route('dashboard'))->withSuccess(__('app.todo_created'));
    }

